bespoke advice, tailored to your needs.</p>
<p>Kempston-Parkes, Chartered Surveyors already works with many
established estate agents in your area, so you can instruct us to survey
your property directly and independently.</p>
</div>
<div class="beta">
<h3>SURVEYS, TAILORED TO YOUR NEEDS</h3>
<p>You may be unclear about what type of survey you will need, so to get
things started, why not call us for a no-obligation consultation over the
phone, so that we can advise you on the best route forward.</p>
<p>We can discuss the type of property in question, its location, age,
size and layout and your aspirations. We will then guide you through
the process and recommend the level of inspection that we feel is
appropriate to your needs.</p>
<p>Once the report is complete, it doesn’t end there. You can expect
ongoing support from us throughout the process and we will be on
hand to answer your questions. If you want us to walk around the
property with you again, to put potential defects into context, then we
can arrange that with you too. It may help to put your mind at ease.
We ultimately aim to offer reassurance amidst uncertainty.</p>
<p>At Kempston-Parkes Chartered Surveyors, we offer four levels
of inspection and produce reports that are appropriate to your
requirements.</p>
</div>
<div class="gamma">
<dl class="accordion">
    <dt class="active"><span class="label"><em>1</em>Property Valuation</span><span class="link"><em>VIEW</em></span></dt>
    <dd><p>This is where we value the property you want to purchase. This inspection will typically take 25-30 minutes and we will
provide you with a short report, detailing the open market valuation and building insurance valuation.</p>
<p>We also carry out Matrimonial Valuations, Valuations for Probate, in fact for any purpose. We will also liaise on your
behalf with Solicitors or the Valuation Office Agency. We are always here to help.</p></dd>
<dt><span class="label"><em>2</em>Home Condition Report</span><span class="link"><em>VIEW</em></span></dt>
<dd><p>A Home Condition Report can take around 30-40 minutes. It does not include a valuation, but takes into account the
constituent parts of the property, and we will make a grading 1-3, depending on the condition of the property:</p>
<ul>
<li>- Grade 1 means that no repairs are required at the current time</li>
<li>- Grade 2 reveals that some repairs are required, but they are not serious or urgent</li>
<li>- Grade 3 represents the fact that there are defects of a serious nature or there are some defects that require urgent repair.</li>
</ul>
</dd>
<dt><span class="label"><em>3</em>HOMEBUYERS’ SURVEY</span><span class="link"><em>VIEW</em></span></dt>
<dd><p>A Homebuyers’ Survey is recommended for the vast majority of properties built from 1850 onwards. The aim of the survey
is to provide a snapshot of the overall condition of the property and to identify those areas that need further investigation.</p>
<p>The inspection can typically take up to two hours, with the output being a 20-page report outlining the condition of
the property, putting any defects into context. A detailed explanation is conveyed to the potential buyer, alongside a
recommendation for any further investigations. A property valuation is also provided as part of the survey.</p></dd>
<dt><span class="label"><em>4</em>Building Survey</span><span class="link"><em>VIEW</em></span></dt>
<dd><p>The Building Survey is a more detailed, comprehensive report about the condition of a property. It can take several hours to
complete and is recommended for properties that are over 150 years old, have been significantly extended or altered or are listed.</p>
<p>The survey inspects every accessible part of the property and examines all defects, outlines repair, and offers technical
information about the property’s construction and materials. Proposals are made for any further special inspections and
subsequent work, which may need to be done on the property.</p></dd>
</dl>
<h3>OUR COSTS</h3>
<p>You will need to ensure that you have the right inspection for your property, offering you peace of mind before you make
that large financial investment.</p>
<p>The costs of our services are dependent on a number of factors including the type of property inspected, its age and value.
We can discuss the expected costs with you before you commission us to carry out an inspection.</p>
